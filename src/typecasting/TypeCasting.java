package typecasting;

public class TypeCasting {

    private Byte dataByte;
    private Integer dataInteger;
    private Long dataLong;
    private Float dataFloat;
    private Double dataDouble;
    private char dataChar;
    private boolean dataBoolean;
    private String dataString;

    public Byte getDataByte() {
        return dataByte;
    }

    public void setDataByte(Byte dataByte) {
        this.dataByte = dataByte;
    }

    public Integer getDataInteger() {
        return dataInteger;
    }

    public void setDataInteger(Integer dataInteger) {
        this.dataInteger = dataInteger;
    }

    public Long getDataLong() {
        return dataLong;
    }

    public void setDataLong(Long dataLong) {
        this.dataLong = dataLong;
    }

    public Float getDataFloat() {
        return dataFloat;
    }

    public void setDataFloat(Float dataFloat) {
        this.dataFloat = dataFloat;
    }

    public Double getDataDouble() {
        return dataDouble;
    }

    public void setDataDouble(Double dataDouble) {
        this.dataDouble = dataDouble;
    }

    public char getDataChar() {
        return dataChar;
    }

    public void setDataChar(char dataChar) {
        this.dataChar = dataChar;
    }

    public boolean isDataBoolean() {
        return dataBoolean;
    }

    public void setDataBoolean(boolean dataBoolean) {
        this.dataBoolean = dataBoolean;
    }

    public String getDataString() {
        return dataString;
    }

    public void setDataString(String dataString) {
        this.dataString = dataString;
    }
}
